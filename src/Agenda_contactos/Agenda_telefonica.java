    private void txttelefonoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txttelefonoKeyTyped
        char c= evt.getKeyChar();
        if((c<'0'||c>'9')&& txttelefono.getText().contains(".")&&(c!=(char)KeyEvent.VK_BACK_SPACE)){
            evt.consume();
            JOptionPane.showMessageDialog(null,"solo se admiten numeros","validar numeros",JOptionPane.INFORMATION_MESSAGE);
        }else if((c<'0'||c>'9')&&(c!='.')
                && (c!=(char)KeyEvent.VK_BACK_SPACE)){
            evt.consume();
            
        }
        
    }//GEN-LAST:event_txttelefonoKeyTyped

    private void txtnombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtnombreKeyTyped
        char c= evt.getKeyChar();
        
        if((c<'a'||c>'z')&&(c<'A'||c>'Z')&&(c!=(char)KeyEvent.VK_BACK_SPACE)&&(c!=(char)KeyEvent.VK_SPACE)){
            evt.consume();
        }
    }//GEN-LAST:event_txtnombreKeyTyped

